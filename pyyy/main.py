import modulo 

def moda(notas):
    moda1 = modulo.mode(notas)
    return moda1

def media(notas):
    media1 = modulo.mean(notas)
    return media1


def mediana(notas):
    mediana1 = modulo.median(notas)
    return mediana1

def desvio(notas):
    desvio1 = modulo.stdev(notas)
    return desvio1





